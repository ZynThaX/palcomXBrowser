package se.lth.cs.palcom.browsergui.views;

import javax.swing.JTextPane;

public class XmlTextPane extends JTextPane {
	private static final long serialVersionUID = 7933398735367894294L;

	public XmlTextPane() {
        this.setEditorKitForContentType("text/xml", new XmlEditorKit());
        this.setContentType("text/xml");
    }
    
}